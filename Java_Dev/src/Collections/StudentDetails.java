package Collections;

public class StudentDetails {
	
	//Declaring Instance Variables
	int sid;
	String sname;
	long sphone;
	boolean graduate;
	
	//Constructor Declaration
	public StudentDetails(int sid, String sname, long sphone, boolean graduate) {
		super();
		this.sid = sid;
		this.sname = sname;
		this.sphone = sphone;
		this.graduate = graduate;
	}
}

